void clear_frame_buffer() {
	short i;
	for(i = 0; i < WIDTH*HEIGHT; i++) {
		if((i % WIDTH == 0) | (i % WIDTH == WIDTH - 1) | i < WIDTH | i >= (WIDTH * (HEIGHT - 1))) {
			frame_buffer[i] = 0xff;
			continue;
		}
		frame_buffer[i] = 0x00;
	}
}